const express = require('express');
const validate = require('../../middlewares/validate');
const snippetValidation = require('../../validations/snippet.validation');
const snippetController = require('../../controllers/snippet.controller');

const router = express.Router();

router.route('/').get(validate(snippetController.getSnippets), snippetController.getSnippets);

router.route('/:snippetId').get(validate(snippetValidation.getSnippet), snippetController.getSnippet);

module.exports = router;

